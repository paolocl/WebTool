<?php

namespace ORMBundle\Entity;

/**
 * Log
 */
class Log
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var integer
     */
    private $idSite;

    /**
     * @var string
     */
    private $urlAdmin;

    /**
     * @var string
     */
    private $userAdmin;

    /**
     * @var string
     */
    private $passAdmin;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set idSite
     *
     * @param integer $idSite
     *
     * @return Log
     */
    public function setIdSite($idSite)
    {
        $this->idSite = $idSite;

        return $this;
    }

    /**
     * Get idSite
     *
     * @return integer
     */
    public function getIdSite()
    {
        return $this->idSite;
    }

    /**
     * Set urlAdmin
     *
     * @param string $urlAdmin
     *
     * @return Log
     */
    public function setUrlAdmin($urlAdmin)
    {
        $this->urlAdmin = $urlAdmin;

        return $this;
    }

    /**
     * Get urlAdmin
     *
     * @return string
     */
    public function getUrlAdmin()
    {
        return $this->urlAdmin;
    }

    /**
     * Set userAdmin
     *
     * @param string $userAdmin
     *
     * @return Log
     */
    public function setUserAdmin($userAdmin)
    {
        $this->userAdmin = $userAdmin;

        return $this;
    }

    /**
     * Get userAdmin
     *
     * @return string
     */
    public function getUserAdmin()
    {
        return $this->userAdmin;
    }

    /**
     * Set passAdmin
     *
     * @param string $passAdmin
     *
     * @return Log
     */
    public function setPassAdmin($passAdmin)
    {
        $this->passAdmin = $passAdmin;

        return $this;
    }

    /**
     * Get passAdmin
     *
     * @return string
     */
    public function getPassAdmin()
    {
        return $this->passAdmin;
    }
}
